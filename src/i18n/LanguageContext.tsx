interface Ctx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  tArray: (key: string) => string[];
}

const LanguageContext = createContext<Ctx | null>(null);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("duck_lang") as Lang | null) : null;
    return saved && ["pt", "en", "es", "fr", "de"].includes(saved) ? saved : "pt";
  });

  useEffect(() => {
    document.documentElement.lang = lang === "pt" ? "pt-BR" : lang;
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("duck_lang", l);
  };

  const t = (key: string) => {
    const v = dictionaries[lang][key] ?? dictionaries.pt[key] ?? key;
    return typeof v === "string" ? v : String(v);
  };
  const tArray = (key: string) => {
    const v = dictionaries[lang][key] ?? dictionaries.pt[key];
    return Array.isArray(v) ? v : [];
  };

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, tArray }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLang = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLang must be used inside LanguageProvider");
  return ctx;
};
